#include "sh_func.h"

void			ft_del_tab(char **tab1)
{
	int			i;

	i = 0;
	if (!tab1)
		return ;
	while (tab1[i])
	{
		free(tab1[i]);
		tab1[i] = NULL;
		i++;
	}
	free(tab1);
}

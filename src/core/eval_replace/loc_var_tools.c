#include "sh_func.h"

int		check_dollar_end(char *str, int i)
{
	if (!str || !str[i])
		return (0);
	if ((str[i] == '\"' || str[i] == '`' || str[i] == '(' || str[i] == ')'
		|| str[i] == ' ' || str[i] == '/') &&
			(check_back_from(str, i - 1) % 2 == 0))
		return (1);
	return (0);
}

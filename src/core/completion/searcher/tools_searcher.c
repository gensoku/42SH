#include "sh_func.h"

int			cls(char *str)
{
	if (!str)
		return (0);
	if (str[0] == '.' && str[1] && str[1] == '/')
		return (1);
	if (str[0] == '.' && str[1] && str[1] == '.' && str[2] && str[2] == '/')
		return (1);
	return (0);
}

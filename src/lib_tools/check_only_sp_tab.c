#include "sh_func.h"

int		chk_tsp(char *str)
{
	int i;

	i = 0;
	if (!str)
		return (0);
	while (str[i])
	{
		if (str[i] != ' ' && str[i] != 9)
			return (1);
		i++;
	}
	return (0);
}
